    <h3>When an app is open source a developer can ...</h3>
    <ul>
      <img src="hacking.jpg" alt="hacking">
      <li>Learn how an app works</li>
      <p style="margin-left: 25px;">Lets say a developer finds a feature in another persons app that they don't understand and want to learn how to have in their own app. They can just look at the code. Most of the time, open source developers publish docs that explain each feature.</p>
      <li>Reveal security concerns</li>
      <p style="margin-left: 25px;">An open source is much more safe than proprietery software beacuse it is impossible to secretlly put ransomware or any other bad stuff integrated in the app.</p>
      <p id="cite" class="attrannounceibution">"<a id="gray" target="_blank" rel="noopener noreferrer" href="https://www.flickr.com/photos/44093100@N00/1186846236">iPhone hacked</a>" by <a id="gray" target="_blank" rel="noopener noreferrer" href="https://www.flickr.com/photos/44093100@N00">bwana</a> is licensed under <a id="gray" target="_blank" rel="noopener noreferrer" href="https://creativecommons.org/licenses/by-nc-sa/2.0/?ref=openverse">CC BY-NC-SA 2.0</a></p>
      <li>Announce shady practices</li>
      <p style="margin-left: 25px;">If an app trys to secretly sell their users data and the code is open source, people will immediately notice an announce it to the public. For instance, Apple was just found tracking their users as soon as the phone is set up without telling the user. Even though they have to tell the user, nobody knew beacuse the code for their phone is not open source.</p>
      <li>Use the code in their own app</li>
      <p style="margin-left: 25px;">This is pretty simple, if somebody wants a feauture, they can use it. This makes the whole technology echosystem a better place. Even small developers have the capability to make an app as good as a company like Google.</p>
      <li>Suggest changes to the code</li>
      <p style="margin-left: 25px;">This is really helpfull especially if you are a solo dev and don't have a team to help fix unknown bugs. If somebody is esspecially good in one field, they can help another developer who is not doing it in the most efficient way</p>
      <li>Make a "fork" of the app</li>
      <p style="margin-left: 25px;">This means the there can be more competition which will drive the app to become better. For instance, if an app is missing some essential features that the public wants, somebody can copy the existing app, adding the needed features, then redestrubuite. Then if somebody really wants that feature, they can just use the fork instead of coding it themselves.</p>
    </ul>
