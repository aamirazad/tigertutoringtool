    <h2 class="text-[#e96c4c] text-2xl">What is open source software?</h2>
    <p class="px-10 pb-10 text-left text-[#4f99aa]">The term 'open source' is used to describe software whose source code is available to the public. Although this may seem uninportant, there are lots of advangatges to having code be open source both for the user and the developer.</p>
    <img class="" src="code-on-wall.jpg" alt="code on wall">
    <p>Github, is a very popluar code hosting platform. When you put your code on github, it is then open source, anyone can access the code. Others can "fork" your code, which is essentally chaning your code so it fits their needs. If a developer wants to imporve another users code, lets say they found a way to make the app 20% faster, they can then make a "pull request". The real developer can than see the pull request and if they like it, can "merge" it to their software.</p>
    <h2>Advantages of open source</h2>
